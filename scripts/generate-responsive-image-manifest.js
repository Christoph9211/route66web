import fs from 'fs'
import path from 'path'
import process from 'node:process'
import { fileURLToPath } from 'url'

const scriptPath = fileURLToPath(import.meta.url)
const __dirname = path.dirname(scriptPath)

const DEFAULT_IMAGES_DIR = path.join(__dirname, '../public/assets/images')
const DEFAULT_OUTPUT_PATH = path.join(
    __dirname,
    '../src/generated/responsiveImageManifest.js'
)

const FORMATS = ['avif', 'webp', 'jpg']
const RESPONSIVE_FILE_PATTERN = /^(.+)-(\d+)w\.(avif|webp|jpg)$/

function intersectSortedWidths(widthSets) {
    if (!widthSets.length) return []
    const [firstSet, ...restSets] = widthSets

    return [...firstSet]
        .filter((width) => restSets.every((set) => set.has(width)))
        .sort((a, b) => a - b)
}

function buildResponsiveImageManifestFromFiles(fileNames) {
    const groupedFormats = new Map()

    for (const fileName of fileNames) {
        const match = fileName.match(RESPONSIVE_FILE_PATTERN)
        if (!match) continue

        const [, baseName, widthRaw, format] = match
        const width = Number(widthRaw)
        if (!Number.isFinite(width) || width <= 0) continue

        if (!groupedFormats.has(baseName)) {
            groupedFormats.set(
                baseName,
                new Map(FORMATS.map((supportedFormat) => [supportedFormat, new Set()]))
            )
        }

        groupedFormats.get(baseName).get(format).add(width)
    }

    const manifest = {}
    const sortedBaseNames = [...groupedFormats.keys()].sort((a, b) =>
        a.localeCompare(b)
    )

    for (const baseName of sortedBaseNames) {
        const byFormat = groupedFormats.get(baseName)
        const widths = intersectSortedWidths(FORMATS.map((format) => byFormat.get(format)))
        if (widths.length > 0) {
            manifest[baseName] = widths
        }
    }

    return manifest
}

async function getTopLevelImageFiles(imagesDir) {
    const entries = await fs.promises.readdir(imagesDir, { withFileTypes: true })
    return entries.filter((entry) => entry.isFile()).map((entry) => entry.name)
}

function buildManifestModuleSource(manifest) {
    const json = JSON.stringify(manifest, null, 4)
    return `// Auto-generated by scripts/generate-responsive-image-manifest.js
// Do not edit manually.
export const responsiveImageManifest = ${json}
`
}

async function writeResponsiveImageManifest({
    imagesDir = DEFAULT_IMAGES_DIR,
    outputPath = DEFAULT_OUTPUT_PATH,
} = {}) {
    const fileNames = await getTopLevelImageFiles(imagesDir)
    const manifest = buildResponsiveImageManifestFromFiles(fileNames)
    const source = buildManifestModuleSource(manifest)

    await fs.promises.mkdir(path.dirname(outputPath), { recursive: true })
    await fs.promises.writeFile(outputPath, source, 'utf8')

    return manifest
}

const isDirectRun =
    process.argv[1] &&
    path.resolve(process.argv[1]) === path.resolve(scriptPath)

if (isDirectRun) {
    writeResponsiveImageManifest()
        .then((manifest) => {
            console.log(
                `Generated responsive image manifest with ${Object.keys(manifest).length} image base(s).`
            )
        })
        .catch((error) => {
            console.error('Failed to generate responsive image manifest:', error)
            process.exitCode = 1
        })
}

export { buildResponsiveImageManifestFromFiles, writeResponsiveImageManifest }
